    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        // TODO add your handling code here:

        if (!"".equals(txtNome.getText())
                && !"".equals(txtMateria.getText())
                && !"".equals(txtNota.getText())) {

            double nota = Double.parseDouble(txtNota.getText());
            Aluno aluno = new Aluno(txtNome.getText(), txtMateria.getText(), Double.parseDouble(txtNota.getText()));
            arrayListALuno.add(aluno);
            atualizaListaAlunos();
        } else {
            JOptionPane.showMessageDialog(this, "Favor Preencher todos os campos", "Campos em Branco", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInserirActionPerformed

