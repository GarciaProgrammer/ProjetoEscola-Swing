    private void btnLimparTudoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparTudoActionPerformed
        String[] vetorString = {""};
        JListAlunos.setListData(vetorString);

        arrayListALuno.removeAll(arrayListALuno);
    }//GEN-LAST:event_btnLimparTudoActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (JListAlunos.getSelectedIndex() > -1) {
            int indiceSelecionado = JListAlunos.getSelectedIndex();
            arrayListALuno.remove(indiceSelecionado);
            atualizaListaAlunos();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um registro", "Registro em branco", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

