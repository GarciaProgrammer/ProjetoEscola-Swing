    public void limparCampos() {
        txtNome.setText("");
        txtMateria.setText("");
        txtNota.setText("");
    }

    public void atualizaListaAlunos() {
        String[] textoLista = new String[arrayListALuno.size()];
        for (int i = 0; i < arrayListALuno.size(); i++) {
            Aluno aluno = getAluno(i);
            textoLista[i] = (i + 1) + " - " + aluno.getNome();
            textoLista[i] += " - " + aluno.getMateria();
            textoLista[i] += " - " + aluno.getNota();

        }
        JListAlunos.setListData(textoLista);
        limparCampos();
    }

    /**
     * retorna um aluno
     *
     * @param indice
     * @return
     */
    public Aluno getAluno(int indice) {
        return arrayListALuno.get(indice);
    }

